import mongoose, { Mongoose } from "mongoose";

const UserSchema = Mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamp: true }
);

const User = mongoose.model("User", UserSchema);

export default User;
